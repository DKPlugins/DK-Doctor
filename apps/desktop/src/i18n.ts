const CMD_NAMES: Record<Lang, Record<number, string>> = { ru: CMD_NAMES_RU, en: CMD_NAMES_EN };

/** Localized short name of an RPG Maker command code (fallback — `Command N`). */
export function cmdName(lang: Lang, code: number): string {
  return CMD_NAMES[lang][code] ?? `Command ${code}`;
}

/** Localized severity label (Errors/Warnings/Info). */
export function sevLabel(lang: Lang, s: Severity): string {
  return t(lang, s === "error" ? "sevError" : s === "warning" ? "sevWarning" : "sevInfo");
}

/** Relative time from the `ts` mark (ms) to now, in short form. */
export function relTime(lang: Lang, ts: number): string {
  const sec = Math.max(0, Math.floor((Date.now() - ts) / 1000));
  if (sec < 60) return t(lang, "justNow");
  const min = Math.floor(sec / 60);
  if (min < 60) return `${min} ${t(lang, "minAgo")}`;
  const hr = Math.floor(min / 60);
  if (hr < 24) return `${hr} ${t(lang, "hAgo")}`;
  const day = Math.floor(hr / 24);
  if (day < 7) return `${day} ${t(lang, "dAgo")}`;
  return new Date(ts).toLocaleDateString(lang === "ru" ? "ru-RU" : "en-US", {
    month: "short",
    day: "numeric",
  });
}
